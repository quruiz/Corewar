#include "includes/libft.h"

int		ft_nbrlen(int n)
{
	int res;

	res = 0;
	if (n == 0)
		res = 1;
	if (n < 0)
		res++;
	while (n != 0)
	{
		n /= 10;
		res++;
	}
	return (res);
}
