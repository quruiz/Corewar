#include "ft_printf.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ret;

	ret = (char *)s;
	i = 0;
	while (i < n)
	{
		ret[i] = '\0';
		i++;
	}
}
