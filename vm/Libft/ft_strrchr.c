#include "includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (*s != '\0')
	{
		i++;
		s++;
	}
	while (i >= 0 && *s != c)
	{
		i--;
		s--;
	}
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
